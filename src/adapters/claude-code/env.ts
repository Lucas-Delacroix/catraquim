export const buildClaudeCodeEnv = (
  homePath: string,
  baseEnv: NodeJS.ProcessEnv = process.env
): NodeJS.ProcessEnv => {
  const env = { ...baseEnv };

  for (const key of CLAUDE_CODE_CLEAR_ENV) {
    Reflect.deleteProperty(env, key);
  }

  return {
    ...env,
    CLAUDE_CONFIG_DIR: expandHome(homePath),
  };
};
